						<h2 class="border-bottom__full">Sort out your will</h2>
						<p>Having a properly drawn up and regularly updated will, provides peace of mind and can avoid unnecessary legal expense. This relatively inexpensive exercise means your assets will pass to those you wish to benefit on your death and reduces the risk of your property (or your estate) passing on to others. If you’re moving house, this is the perfect time to take care of your will.</p>
						<p>Drawing up a will usually involves naming the people (beneficiaries) who you would like to inherit your property. It needs to be a formal legal document and be correctly signed by appropriate parties. This avoids it being declared invalid, in which case your property and assets may not go to the people you intended.</p>
						<p>Having a properly drawn up will can help to avoid certain pitfalls, such as:</p>
						<ul>
							<li>Legal rights – without a will claims could be made on your property by children or a surviving spouse</li>
							<li>Intestate succession – if you leave no will, the law will decide what will happen to your estate</li>
							<li>Non-married couples – your partner may get nothing from your estate if you have no will, no matter how long you have been living together</li>
							<li>Inheritance tax – inheritance tax may be liable to be paid on the value of even a modest house, but having a will can help minimise this</li>
							<li>Home-made wills – it is not normally sensible to try and draw up a will yourself. If anything is unclear the consequences can be disastrous</li>
						</ul>
					</section>
					<section class="content-padding--sub">
						<h2 class="border-bottom__full">Set up your power of attorney</h2>
						<p>A power of attorney is simply a written document in which you allow someone else to manage your affairs on your behalf. Once in place it will give you and your family peace of mind and can help prevent stressful situations from developing. It avoids difficult decisions being made, such as who will access your bank account, pay your family’s bills, decide where you should live, run your business or indeed manage any other legal or financial affairs for you.</p>
						<p>A power of attorney could be used to cover periods when you may be abroad for a significant amount of time or it could be used in later life when, either through physical or mental illness, it can become more difficult to manage your own affairs yourself.</p>
						<p>Most of us should think about putting a Power of Attorney in place. Let us use our knowledge and experience to advise on the most appropriate option for you.</p>
					</section>
					<section class="content-padding--sub">
						<h2 class="border-bottom__full">Organise your executries</h2>
						<p>Executry is the process of dealing with a deceased estate, whether or not there is a will. The executor is the person or persons who are legally empowered to administer and deal with the assets of the deceased. The executor is usually responsible for pulling together information about items owned by the deceased which are used to prepare an inventory of the estate. This usually has to be approved by the court. Forms need to be completed to address any tax that is due and, once everything is approved by the Court, confirmation will be granted in favour of the executor. This means the estate can then be wound up.</p>
						<p>We can deal with all aspects of the winding up of the deceased’s estate and ensure successful distribution of the assets, including mitigation of Inheritance Tax.</p>
					</section>
					<section class="content-padding--sub">
						<h2 class="border-bottom__full">Take care of your trusts</h2>
						<p>A trust allows assets to be held by one group of people - the trustees - on behalf of another group of people - the beneficiaries. For example, trusts are often used when the beneficiaries are children so that money or property can be managed for them until they become adults. Trusts may also be used to mitigate Inheritance Tax and Capital Gains Tax.</p>
						<p>Usually, a formal written document known as a trust deed is prepared. This will detail what the purpose of the trust is, what is to be done with the trust assets and when the beneficiaries are to receive them.</p>
						<p>We can take care of the creation of trusts and also handle their administration.
